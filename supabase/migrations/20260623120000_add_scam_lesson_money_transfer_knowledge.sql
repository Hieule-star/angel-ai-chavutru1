Tiền bạc càng phải sáng rõ. Quan hệ càng thân càng phải minh bạch.

## Bài học 3 - Nhìn sâu vào nhân quả

Khi mất tiền, điều đầu tiên không phải là oán trách, hoảng loạn hay nguyền rủa. Hãy dừng lại và nhìn sâu. Đây có thể là bài học về thiếu tỉnh thức, cả tin, lòng tham, chủ quan, kiêu mạn, hoặc từng bước vào những chuyện lập lờ không minh bạch.

Người có tu học nên quay về với **Sám Hối** và **Biết Ơn**:

