## Bài học từ một kiểu lừa đảo rất tinh vi

Có một kiểu lừa đảo tiền bạc rất tinh vi thường nhắm vào người thông minh, lanh lợi, có uy tín và có quan hệ thân thiết với nạn nhân thật. Điểm nguy hiểm là kẻ xấu không nhất thiết lừa trực tiếp người chuyển tiền đầu tiên, mà lợi dụng một người trung gian đáng tin để tạo niềm tin.

## Kịch bản lừa đảo phổ biến

Ví dụ có một người thật tên A. Kẻ lừa đảo giả mạo A trên Zalo, Messenger hoặc một kênh nhắn tin khác. Ta gọi kẻ giả mạo là "A giả".

A giả liên hệ với một người tên B. B là người có uy tín, thân thiết với A thật và được A thật tin tưởng. A giả nhờ B gọi cho A thật, làm như B đang hỏi mượn tiền A. A giả có thể tạo lý do kiểu "em có việc khó nói với gia đình nên nhờ chị đứng ra hỏi mượn giúp".

Vì B là người thân, có uy tín và được A thật tin tưởng, A thật có thể tin ngay và chuyển tiền vào tài khoản của B. Sau khi tiền vào tài khoản B, A giả tiếp tục nhắn B chuyển số tiền đó sang một tài khoản khác, nói rằng cần trả gấp cho người ta.

B nghĩ đơn giản rằng tiền là của A, A nhờ chuyển đi đâu thì mình chuyển. Kết quả là tiền đi vào tài khoản của kẻ lừa đảo. Khi phát hiện thì đã quá muộn.

