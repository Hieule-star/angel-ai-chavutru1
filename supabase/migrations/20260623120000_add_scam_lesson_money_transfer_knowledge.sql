- Sám hối vì mình còn tâm tham, tâm vội hoặc tâm muốn giải quyết nhanh.
- Biết ơn vì của đi thay người.
- Biết ơn vì mất tiền là mất ít; còn người, còn phước, còn cơ hội tỉnh ra là còn tất cả.
