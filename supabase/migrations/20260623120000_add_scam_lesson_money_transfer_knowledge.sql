## Bài học 1 - Luôn tỉnh táo và sáng suốt

Khi tâm yếu, hoảng, vội, cả tin hoặc thiếu định lực, con người rất dễ bị dẫn dắt. Kẻ xấu thường tạo cảm giác gấp gáp, bí mật, khó nói, thương cảm để người bị lừa không kịp suy xét.

Người có thiền định, định lực và tỉnh thức sẽ không dễ bị cuốn theo năng lượng hoảng loạn đó. Với mọi việc liên quan đến tiền bạc, cần dừng lại, thở sâu, kiểm tra kỹ và xác minh trực tiếp.

## Bài học 2 - Mọi việc phải rõ ràng, minh bạch

